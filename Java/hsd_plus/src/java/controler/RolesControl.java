    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String nombre=request.getParameter("nombre");
        String fecha_apertura =request.getParameter("fecha_apertura ");
        Roles r=new Roles();
        r.setNombre(nombre);
        r.setFecha_apertura(fecha_apertura );
        if(RolesDao.registrar(r)){
            request.setAttribute("mensaje","El Rol Fue Registrado con Exito");
        }else {
            request.setAttribute("mensaje","El Rol Nooooooooo Fue Registrado con Exito");
        }
        request.getRequestDispatcher("registroRoles.jsp").forward(request, response);
    }

